                    <h3 class="set-left"><span class="htb3">Things to consider before deciding on an instrument</span></h3>
                    <ul class="list-group">
                        <li class="list-group-item">It is important that the student has somewhere to practise, where they will not be disturbed by noise (TV) or siblings. It is good if they have access to a music player to listen to recordings and a safe place to store their instrument when it is not in use.  They will also need a music stand which, though collapsible, is more convenient if left assembled.</li>
                    </ul>
                    <ul class="list-group">
                        <li class="list-group-item"> Overhearing your child practising really is one of the benefits of them learning an instrument, even when they are a total beginner. However you do need to consider your neighbours, who may not share that proud glow when they achieve their first few notes.</li>
                    </ul>
                    <ul class="list-group">
                        <li class="list-group-item">All children can learn an instrument but they will progress at different rates according to age and aptitude. As long as they are usually enjoying themselves and making progress they, and you, should be satisfied. However the amount of practice, and in particular the frequency of practice, will make a huge impact on how quickly they learn. Learning an instrument develops both emotional and communicative maturity – but it is also a physical activity. Playing an instrument develops the strength and coordination of specific muscles which need regular ‘workouts’.  Some instruments are more demanding in this respect than others: brass and reed instruments need regular practise to build up embouchure stamina (and the ‘lip’ will not last for a full lesson if it has not been used during the week). The same applies to finger muscles and arm muscles.
                        </li>
                    </ul>
                    <ul class="list-group">
                        <li class="list-group-item">With all instruments there are times where rapid progress is made – and then times when it seems to be a real struggle to get over a hurdle to the next stage. All players go through these stages and need encouragement to persevere, as afterwards they will find playing much more rewarding and enjoyable.</li>
                    </ul>
