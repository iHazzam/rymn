                    <h2 class="set-left"><span class="htb6">Where to get the things you need</span></h2>
                    <h3 class="set-left"><span class="htb3">Getting hold of an instrument</span></h3>
                    <p>
                        When you have a good idea what instrument(s) your child might like to learn, the next question is how to get hold of an instrument for them to play.  They may wish to try several before making a firm decision.
                        <br>
                        There is no point in buying an instrument until your child has decided exactly which instrument they want to learn and has shown that they want to make a firm commitment to continuing to learn to play it.
                        <br>
                        Your first port of call should be your school and/or, through them, the <a href="http://www.northyorks.gov.uk/article/26329/Assisted-purchase-scheme-for-musical-instruments">County Music Service (Music Hub)</a>.  They possess a large stock of instruments of all kinds and you may very well be able to borrow one – or hire one at minimum rate.
                </div>
                <div class="col-sm-3">
                    <section class="ht-box2"><div><span class="helper"></span><img class="img_small" src="/imgs/music_shop.jpg"></div></section>
                </div>
                <div class="col-xs-12">
                    Failing that, there are a number of <a href="https://www.yell.com/s/musical+instrument+and+sheet+music+shops-north+yorkshire.html">music shops</a> in the area, some of which operate reasonably priced hire schemes for school children. Instruments vary widely in price and can be purchased outright, bought on a hire purchase arrangement with a music shop, or leased. There are small sized instruments (mainly strings) for younger players, which cost less than the full sized instruments.
                    <br>
                    When you do want to buy your own instrument, it makes sense to buy one that will retain its value, so that you can sell it again if need be (if your child gives up – or, better still, needs to get a better instrument when they reach a higher level of playing). For our guide on buying instruments, please scroll down!
                    <br>
                    Always consult with your child’s teacher before buying any instrument!  It is worth getting advice about which size to buy and which model, because cheap or second hand instruments sometimes do not work, and a new player can blame themselves. If you buy second hand, always take the instrument to a decent repairer for a thorough overhaul. <a href="/learn/maintainance">(See our guide on instrument repairers in the county)</a>
                    <br>
                    Prices tend to get higher as instruments within each ‘family’ get bigger (so a starter cello costs more than a starter violin). Starter string instruments are cheaper than brass, which are in turn cheaper than woodwind. ‘Shortage’ instruments can sometimes be leased from County Music Services at very low cost or for nothing if you have a County teacher. Looking at guide prices on <a href="http://www.ebay.co.uk">eBay</a> is probably a good idea, buying instruments on eBay probably isn’t!
                    <br>
                    There are some other, ongoing costs for some instruments:-
                    <br>
                    </p>
                    <ul>
                        <li><span class="htb1">Strings</span> - you will need to buy/make a shoulder rest (violin & viola), a slip-stop or spike holder (Cello & D. Bass) and buy rosin (all bowed instruments). Occasionally strings will require replacing and bows need re-hairing every few years..</li>
                        <li><span class="htb2">Woodwind</span> -   need wiping out after they have been used, a combination of a brush and/or a pull-through, depending on the instrument.</li>
                        <li><span class="htb3">Reed instruments</span> need new reeds regularly. Clarinet and saxophone reeds are relatively cheap, oboe and bassoon reeds cost more. </li>
                        <li><span class="htb4">Music</span> will also need to be purchased . Teachers are very aware that this can add up and do try to keep purchases to a minimum. They may be able to arrange for other pupils to donate music they have finished using. (also see our Facebook page!)</li>
                        <li><span class="htb5">Exams </span> are a way for your children to show and prove their progress. These have entry fees and will also require an accompanist. (See our guide to <a href="/learn/accompanists">“Finding an accompanist”</a>)</li>
                    </ul>
